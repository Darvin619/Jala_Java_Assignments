package javaBasics;

public class VariableTypes {

		//Creating a Global variable as "Variable"
	
		static int Variable = 10;
		
	public static void main(String[] args) {
		
		//Creating a Local variable also same as "Variable"
		
		int Variable = 15;
		
		System.out.println("Global Variable: "+ VariableTypes.Variable);
		
		System.out.println("Local variable: "+ Variable);

	}

}
